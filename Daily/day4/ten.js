//Switch case

let number=10

switch(number){
    case 1:
    console.log("Number is one")
    break
    case 4:
    console.log("Number is four")
    break
    case 10:
    console.log("Number is ten")
    break
    case 20:
    console.log("Number is twenty")
    break
    default:
    console.log("Number is invalid")
}